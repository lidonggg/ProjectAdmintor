package com.admin.projectadmintor.mvp.model.entity;

public class WeatherForecastWind {
    private String sc;
    private String spd;
    private String deg;
    private String dir;

    public String getSc() {
        return this.sc;
    }

    public void setSc(String sc) {
        this.sc = sc;
    }

    public String getSpd() {
        return this.spd;
    }

    public void setSpd(String spd) {
        this.spd = spd;
    }

    public String getDeg() {
        return this.deg;
    }

    public void setDeg(String deg) {
        this.deg = deg;
    }

    public String getDir() {
        return this.dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }
}
